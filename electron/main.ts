import { app, BrowserWindow } from 'electron'
import path from 'node:path'
import { setupSSHHandler } from './ssh'
import { setupAIHandler } from './ai'

process.env.DIST = path.join(__dirname, '../dist')
process.env.VITE_PUBLIC = app.isPackaged ? process.env.DIST : path.join(process.env.DIST, '../public')

let win: BrowserWindow | null

// ðŸš§ Use ['ENV_NAME'] avoid vite:define plugin - Vite@2.x
const VITE_DEV_SERVER_URL = process.env['VITE_DEV_SERVER_URL']

function createWindow() {
  win = new BrowserWindow({
    width: 1200,
    height: 800,
    backgroundColor: '#09090b', // Match bg-zinc-950
    titleBarStyle: 'hiddenInset', // Mac-like title bar
    trafficLightPosition: { x: 12, y: 12 }, // Adjust traffic light position
    webPreferences: {
      preload: path.join(__dirname, 'preload.js'),
    },
  })

  // Setup SSH handlers
  setupSSHHandler(win);
  
  // Setup AI handlers
  setupAIHandler();

  if (VITE_DEV_SERVER_URL) {
    win.loadURL(VITE_DEV_SERVER_URL)
  } else {
    // win.loadFile('dist/index.html')
    win.loadFile(path.join(process.env.DIST, 'index.html'))
  }
}

// Quit when all windows are closed, except on macOS. There, it's common
// for applications and their menu bar to stay active until the user quits
// explicitly with Cmd + Q.
app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit()
  }
})

app.on('activate', () => {
  // On OS X it's common to re-create a window in the app when the
  // dock icon is clicked and there are no other windows open.
  if (BrowserWindow.getAllWindows().length === 0) {
    createWindow()
  }
})

app.whenReady().then(createWindow)
